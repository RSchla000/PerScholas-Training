package PA30341PracticeAssignmentControlFlow;

public class ControlFlow03 {
    static void main(String[] args) {
        int x = 25;
        if (x<10) {
            System.out.println("Less then 10");
        }else if (x>10 && x<20) {
            System.out.println("Greater then 10 but less then 20");
        }
        else{
            System.out.println("Greater then 20");
        }
    }
}
